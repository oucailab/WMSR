class LFSSM(nn.Module):
    def __init__(
            self,
            hidden_dim: int = 0,
            drop_path: float = 0,
            norm_layer: Callable[..., torch.nn.Module] = partial(nn.LayerNorm, eps=1e-6),
            attn_drop_rate: float = 0,
            d_state: int = 16,
            expand: float = 2.,
            is_light_sr: bool = False,
            **kwargs,
    ):
        super().__init__()
        self.ln_1 = norm_layer(hidden_dim)
        self.self_attention = SS2D(d_model=hidden_dim, d_state=d_state,expand=expand,dropout=attn_drop_rate, **kwargs)
        self.drop_path = DropPath(drop_path)
        self.skip_scale= nn.Parameter(torch.ones(hidden_dim))
        self.conv_blk = LFE(hidden_dim, hidden_dim)
        self.ln_2 = nn.LayerNorm(hidden_dim)
        self.skip_scale2 = nn.Parameter(torch.ones(hidden_dim))
    def forward(self, x):
    #input"bhwc
         # [B,H,W,C]
        x = x.permute(0, 2, 3, 1).contiguous()
        x = self.ln_1(x)
        x = x*self.skip_scale + self.drop_path(self.self_attention(x))
        x = x*self.skip_scale2 + self.conv_blk(self.ln_2(x).permute(0, 3, 1, 2).contiguous()).permute(0, 2, 3, 1).contiguous()
        return x.permute(0, 3, 1, 2).contiguous() # [B,H,W,C]

class ResBlock(nn.Module):
    def __init__(self, conv, n_feats, kernel_size, bias=True, bn=False, act=nn.ReLU(True), res_scale=1):

        super(ResBlock, self).__init__()
        m = []
        for i in range(2):
            m.append(conv(n_feats, n_feats, kernel_size, bias=bias))
            if bn:
                m.append(nn.BatchNorm2d(n_feats))
            if i == 0:
                m.append(act)

        self.body = nn.Sequential(*m)
        self.res_scale = res_scale

    def forward(self, x):
        res = self.body(x).mul(self.res_scale)
        res += x

        return res
def default_conv(in_channels, out_channels, kernel_size, bias=True):
    return nn.Conv2d(
        in_channels, out_channels, kernel_size,
        padding=(kernel_size//2), bias=bias)


class WAM(nn.Module):
    def __init__(
            self,
            hidden_dim: int = 60,
            **kwargs,
    ):
        super().__init__()
        self.dwt = DWT()
        self.iwt = IWT()
        self.lfssm = LFSSM(hidden_dim)
        self.wsa = DEConv(dim=hidden_dim*3)
        self.conv_blk = ResBlock(default_conv, hidden_dim, 3)
        self.ln_2 = nn.LayerNorm(hidden_dim)
        self.skip_scale2 = nn.Parameter(torch.ones(hidden_dim))
        self.conv = nn.Sequential(nn.Conv2d(hidden_dim, hidden_dim, 3, 1, hidden_dim), nn.LeakyReLU(negative_slope=0.2, inplace=True))
    def forward(self, x):
        x1 = self.conv(x)
        a, b = self.dwt(x)#bchw
        a1 = self.lfssm(a)#bhwc
        b1 = self.wsa(b) + b
        x2 = torch.concat((a1, b1),1)#BCHW
        x3 = self.iwt(x2)
        x4 = self.ln_2(x3.permute(0, 2, 3, 1).contiguous()).permute(0, 3, 1, 2).contiguous()
        #torch.cuda.empty_cache()
        return x4

